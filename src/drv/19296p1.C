/*延时：1 毫秒的i 倍*/
void delay(int i)
{
	int j,k;
	for(j=0;j<i;j++)
	for(k=0;k<110;k++);
}
/*延时：1us 的i 倍*/
void delay_us(int i)
{
	int j,k;
	for(j=0;j<i;j++)
	for(k=0;k<1;k++);
}

