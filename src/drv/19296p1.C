//=============transfer command to LCM===============
void SEND_CMD_LCD(uchar data1)
{
	LCD19296_CS1=0;
	LCD19296_RS=0;
	
	LCD19296_RD=0;
	delay_us(1);
	LCD19296_WR=0;
//	P2=data1;
	LCD_Send_PData(data1);
	LCD19296_RD=1;
	delay_us(1);
	LCD19296_CS1=1;
	LCD19296_RD=0;
}

//-----------transfer data to LCM---------------
void SEND_DATA_LCD(uchar data1)
{
	LCD19296_CS1=0;
	LCD19296_RS=1;
	LCD19296_RD=0;
	delay_us(1);
	LCD19296_WR=0;
//	P2=data1;
	LCD_Send_PData(data1);
	LCD19296_RD=1;
	delay_us(1);
	LCD19296_CS1=1;
	LCD19296_RD=0;
}

